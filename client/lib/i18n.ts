export type Language = 'en' | 'de';

export function resolveLanguageFromLocale(
  locale: string | null | undefined
): Language {
  const normalizedLocale = locale?.toLowerCase();

  if (normalizedLocale?.startsWith('de')) {
    return 'de';
  }

  return 'en';
}

